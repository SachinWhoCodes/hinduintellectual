                    <p><strong>Hindi:</strong></p><p><br>कर्म, सनातन धर्म का एक केंद्रीय सिद्धांत, कारण और प्रभाव का सार्वभौमिक नियम है। यह बताता है कि हर क्रिया, चाहे वह शारीरिक हो, मानसिक हो, या भावनात्मक, परिणाम उत्पन्न करती है जो हमारे भविष्य के अनुभवों को आकार देती है। ‘कर्म’ शब्द संस्कृत के ‘कृ’ से लिया गया है, जिसका अर्थ है ‘करना’ या ‘अभिनय’। कर्म इस बात पर जोर देता है कि हम अपने भाग्य के निर्माता हैं।</p><p>&nbsp;</p><p>&nbsp;</p><p>कर्म तीन स्तरों पर कार्य करता है:</p><p>&nbsp;</p><p><strong>संचित कर्म:</strong> पिछले जन्मों से संचित कर्म।</p><p>&nbsp;</p><p><strong>प्रारब्ध कर्म:</strong> वर्तमान जीवन में अनुभव किए जा रहे कर्म।</p><p>&nbsp;</p><p><strong>क्रियमाण कर्म:</strong> वर्तमान में किए गए कर्म, जो भविष्य को प्रभावित करेंगे।</p><p>&nbsp;</p><p>यह सिद्धांत जवाबदेही को प्रोत्साहित करता है, यह सिखाते हुए कि अच्छे कार्य (धर्म के अनुरूप) सकारात्मक परिणाम लाते हैं, जबकि नकारात्मक कार्य चुनौतियों का कारण बनते हैं। हालांकि, कर्म दंडात्मक नहीं है; यह आध्यात्मिक विकास के लिए एक साधन है, जो व्यक्ति को उनकी क्रियाओं से सीखने और दिव्य के साथ संरेखित करने की अनुमति देता है।</p><p>&nbsp;</p><p>सनातन धर्म भक्ति (भक्ति) और ज्ञान (ज्ञान) जैसे मार्ग भी प्रदान करता है, जो अंततः मोक्ष प्राप्त करने में मदद करते हैं, जहां आत्मा जन्म और पुनर्जन्म के चक्र से मुक्त हो जाती है।</p><p>&nbsp;</p><p>&nbsp;</p><p>&nbsp;</p><p><strong>English:</strong></p><p><br>Karma, a central tenet of Sanatan Dharma, is the universal law of cause and effect. It states that every action, whether physical, mental, or emotional, generates consequences that shape our future experiences. Derived from the Sanskrit word ‘Kri,’ meaning ‘to do’ or ‘to act,’ karma emphasizes that we are the architects of our destiny.</p><p>&nbsp;</p><p>&nbsp;</p><p>Karma operates on three levels:</p><p>&nbsp;</p><p><strong>Sanchita Karma:</strong> The accumulated actions from past lives.</p><p>&nbsp;</p><p><strong>Prarabdha Karma:</strong> The portion of karma we are experiencing in this lifetime.</p><p>&nbsp;</p><p><strong>Kriyamana Karma:</strong> The actions we perform now, which will affect our future.</p><p>&nbsp;</p><p>This law encourages accountability, teaching that good deeds (aligned with dharma) lead to positive outcomes, while negative actions result in challenges. However, karma is not punitive; it is a tool for spiritual growth, allowing individuals to learn from their actions and align with the divine.</p><p>&nbsp;</p><p>Sanatan Dharma also offers paths like Bhakti (devotion) and Jnana (knowledge) to transcend karma, ultimately achieving Moksha, where the soul is liberated from the cycle of birth and rebirth.</p>

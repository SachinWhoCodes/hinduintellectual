                    <p><strong>Hindi:</strong></p><p><br>कर्म का सिद्धांत सनातन धर्म का एक मूलभूत विचार है, जो कारण और प्रभाव के सिद्धांत का प्रतिनिधित्व करता है। यह सिखाता है कि प्रत्येक कर्म का परिणाम होता है, जो इस जीवन या भविष्य के जीवन में प्रकट होता है। कर्म एक पुरस्कार और दंड की प्रणाली नहीं है, बल्कि एक प्राकृतिक नियम है जो ब्रह्मांडीय संतुलन और न्याय सुनिश्चित करता है।</p><p>&nbsp;</p><p>हिंदू कर्म के सिद्धांत में विश्वास करते हैं क्योंकि यह धर्म (धार्मिकता) की अवधारणा के साथ मेल खाता है। धर्मपूर्ण कर्मों के द्वारा व्यक्ति सकारात्मक कर्म अर्जित करता है, जिससे सुख और आध्यात्मिक विकास होता है। इसके विपरीत, नकारात्मक कर्मों से दुख और आध्यात्मिक ठहराव होता है। यह विश्वास व्यक्तियों को जिम्मेदारी और नैतिकता के साथ कार्य करने के लिए प्रेरित करता है।</p><p>&nbsp;</p><p>कर्म मानव अनुभवों की विविधता को भी समझाता है। धन, स्वास्थ्य और परिस्थितियों में अंतर को पिछले कर्मों के परिणाम के रूप में देखा जाता है। हालांकि, आत्म-प्रयास, भक्ति, और धर्मपूर्ण जीवन के माध्यम से, व्यक्ति पिछले कर्मों के प्रभावों को पार कर सकता है और मोक्ष (मुक्ति) की ओर बढ़ सकता है।</p><p>&nbsp;</p><p>भगवद गीता में यह बताया गया है कि अपने कर्तव्यों का निःस्वार्थ पालन करते हुए कर्म के फलों से आसक्ति को त्यागना (कर्म योग) कर्म को भंग करने और आध्यात्मिक स्वतंत्रता की ओर ले जाता है।</p><p>&nbsp;</p><p>&nbsp;</p><p>&nbsp;</p><p><strong>English:</strong></p><p><br>The law of karma is a foundational concept in Sanatan Dharma, representing the principle of cause and effect. It teaches that every action (karma) performed by an individual has consequences, which manifest in this life or future lives. Karma is not a system of reward and punishment but a natural law that ensures cosmic balance and justice.</p><p>&nbsp;</p><p>Hindus believe in the law of karma because it aligns with the concept of Dharma (righteousness). By performing righteous actions, individuals accumulate positive karma, leading to happiness and spiritual growth. Conversely, negative actions lead to suffering and spiritual stagnation. This belief encourages individuals to act responsibly and ethically.</p><p>&nbsp;</p><p>Karma also explains the diversity of human experiences. Differences in wealth, health, and circumstances are seen as results of past karma. However, through self-effort, devotion, and righteous living, one can overcome the effects of past karma and progress toward liberation (Moksha).</p><p>&nbsp;</p><p>The Bhagavad Gita emphasizes that detachment from the fruits of actions while performing one’s duties selflessly (Karma Yoga) helps dissolve karma, leading to spiritual freedom.</p>
